DROP TABLE IF EXISTS game_zenit;

CREATE TABLE game_zenit (
    id bigint GENERATED BY DEFAULT AS IDENTITY,
    opdt timestamp with time zone NOT NULL DEFAULT now(),
    oddt timestamp with time zone,
    idmt bigint,
    cone varchar(255),
    ctwo varchar(255),
    idlg varchar(255),
    cfw1 double,
    cfdw double,
    cfw2 double,
    cftmin double,
    tota double,
    cftmax double,
    cf1x double,
    cf12 double,
    cfx2 double,
    for1 double,
    cff1 double,
    for2 double,
    cff2 double,
    scde bigint,
    tone1 bigint,
    tone2 bigint,
    ttwo1 bigint,
    ttwo2 bigint,
    canc varchar(255),
    CONSTRAINT pk_zenit PRIMARY KEY (id)
);